    /**
     * Generate CSS variables cho một màu
     */
    public static function generateCssVariables(?string $colorName = 'red'): string
    {
        $colorName = $colorName ?? 'red';
        $palette = self::$colors[$colorName] ?? self::$colors['red'];

        $css = ":root {\n";
        foreach ($palette as $shade => $hex) {
            $css .= "    --color-primary-{$shade}: {$hex};\n";
        }
        $css .= "}\n";

        return $css;
    }

    /**
     * Lấy hex color theo shade
     */
    public static function getColor(?string $colorName = 'red', int $shade = 600): string
    {
        $colorName = $colorName ?? 'red';
        return self::$colors[$colorName][$shade] ?? self::$colors['red'][$shade];
    }

    /**
     * Lấy toàn bộ palette của một màu
     */
    public static function getPalette(?string $colorName = 'red'): array
    {
        $colorName = $colorName ?? 'red';
        return self::$colors[$colorName] ?? self::$colors['red'];
    }
